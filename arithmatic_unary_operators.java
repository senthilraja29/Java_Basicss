package Hello_world;

public class arithmatic_unary_operators {

	public static void main(String arg[]){
		int a = 25;
		int b = 30;
		System.out.println("value of {addition} is a + b		: " + (a+b));
		System.out.println("value of {Subtraction} is a - b 	: " + (a-b));
		System.out.println("value of {Multiplication} is a * b 	: " + (a*b));
		System.out.println("value of {Division} is a / b 		: " + (a/b));
		System.out.println("value of {Module} is :  a % b		: " + (a%b));
		
		System.out.println("---------------------------------------------");
		// Unirary is have (+,-,++,--,! )
		System.out.println("value of (+) a is : " + (+a));
		System.out.println("value of (-) a is : " + (-a));
		System.out.println("value of (++) a is : " + (++a));
		System.out.println("value of (--) a is : " + (--a));
		System.out.println("value of (!) a is : " + (a));
		
		System.out.println("---------------------------------------------");
	
	
	}

}
