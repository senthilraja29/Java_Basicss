package Day09_11July;

public class Assignment_010_for_loop {
	
	// -10 to 10 by using for loop
	
	void display() {
		
		for(int a = -10; a <= 10; a++ )
		{
			System.out.println(a);
		}
	}
	
	public static void main(String[] args) {
		
		Assignment_010_for_loop a1 = new Assignment_010_for_loop();
		a1.display();
	}
}
